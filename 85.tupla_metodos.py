#%% Métodos de tuplas

tupla = (1, 2, 3, 3, 3)
# count()
print("Numéro de 3 en tupla:", tupla.count(3))
# index()
print("El 1 se encuentra en:", tupla.index(1))
print("El 2 se encuentra en:", tupla.index(2))
print("El 3 se encuentra en:", tupla.index(3))

